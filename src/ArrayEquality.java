import java.util.Arrays;

public class ArrayEquality {
    public static void main(String[] args){

        //program to test equality of 2 arrays

        int a[] = new int[] {1,2,3,4,5};  //declaration, instantiation and initialization of array 1

        int b[] = new int[] {1,2,3,4,5};  //declaration, instantiation and initialization of array 2

        System.out.println(Arrays.equals(a,b));//comparing a and b arrays for single dimensional array only

    }
}

